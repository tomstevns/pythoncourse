# Sequences
# Sequences er lidt ligesom lister. Fornu er det, det vi betragter dem som, selv om de er noget andet.

# range returner en sequence af numre fra start til slut for et givent interval.

print(range(5))  # range(0, 5)

print(list(range(5)))  # [0, 1, 2, 3, 4]

print(list(range(3, 10))) # [3, 4, 5, 6, 7, 8, 9]

print(list(range(3, 20, 4))) # [3, 7, 11, 15, 19]

print(list(range(30, 10, -2))) # [30, 28, 26, 24, 22, 20, 18, 16, 14, 12]
